
package lab04b;


public class Point {
    
    //Instance fields
    
    int x;
    int y;
    
    //Constructor

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
    
    
    
    
    
}
